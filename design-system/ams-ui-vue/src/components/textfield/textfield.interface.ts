export interface TextfieldInterface {
  /**
   * set after of the component
   */
  after?: any
  /**
   * set before of the component
   */
  before?: any
  /**
   * set iconLeft of the component
   */
  iconLeft?: any
  /**
   * set iconRight of the component
   */
  iconRight?: any
  /**
   * set label of the component
   */
  label?: any
  /**
   * set loading of the component
   */
  loading?: boolean
  /**
   * set size of the component
   */
  size?: 'sm' | 'md' | 'lg'
  /**
   * set placeholder of the component
   */
  placeholder?: string
}